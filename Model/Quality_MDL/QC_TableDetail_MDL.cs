using System;
using System.Collections.Generic;
using System.Text;

namespace Admin.Model.Quality_MDL
{
    public class QC_TableDetail_MDL
    {
        private int _ID;
        private string _MasterID;
        private string _DispatchNo;
        private string _BadReasonID;
        private string _ReasonName;
        private string _BadQty;
        

       public QC_TableDetail_MDL(){}
       public QC_TableDetail_MDL(int vID, string vMasterID, string vDispatchNo, string vBadReasonID, string vReasonName, string vBadQty)
       {
           this._ID = vID;
           this._MasterID = vMasterID;
           this._DispatchNo = vDispatchNo;
           this._BadReasonID = vBadReasonID;
           this._ReasonName = vReasonName;
           this._BadQty = vBadQty;
       }

       public int ID
       {
           get { return _ID; }
           set { _ID = value; }
       }

       public string MasterID
       {
           get { return _MasterID; }
           set { _MasterID = value; }
       }

       public string DispatchNo
       {
           get { return _DispatchNo; }
           set { _DispatchNo = value; }
       }

       public string BadReasonID
       {
           get { return _BadReasonID; }
           set { _BadReasonID = value; }
       }
       
       public string ReasonName
       {
           get { return _ReasonName; }
           set { _ReasonName = value; }
       }

       public string BadQty
       {
           get { return _BadQty; }
           set { _BadQty = value; }
       }
    }
}
