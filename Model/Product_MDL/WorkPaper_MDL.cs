using System;
using System.Collections;
using System.Collections.Generic;
using System.Text;

namespace Admin.Model.Product_MDL
{
    public class WorkPaper_MDL:LikeQuery
    {
        private string _ID;
        private string _ProdCode;

        
        private byte[] _WorkGuidanceImg;

      
        public WorkPaper_MDL() {}
        public WorkPaper_MDL(string vID,string vProdCode,byte[] vWorkGuidanceImg
            )
        {
            this._ID = vID;
            this._ProdCode = vProdCode;
            this._WorkGuidanceImg = vWorkGuidanceImg;
        }
        
        public string ID
        {
            get { return _ID; }
            set { _ID = value; }
        }
        public string ProdCode
        {
            get { return _ProdCode; }
            set { _ProdCode = value; }
        }
        public byte[] WorkGuidanceImg
        {
            get { return _WorkGuidanceImg; }
            set { _WorkGuidanceImg = value; }
        }
       
    }
}