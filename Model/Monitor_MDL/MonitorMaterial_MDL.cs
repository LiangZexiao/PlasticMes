using System;
using System.Collections.Generic;
using System.Text;

namespace Admin.Model.Monitor_MDL
{
    public class MonitorMaterial_MDL
    {
        private string _DispatchOrderNo;

        private string _WorkOrderNo;

        private string _MouldNo;

        private string _MachineNo;

        private string _ProductNo;

        private string _ProductDesc;

        private string _MaterialCode;
                
        private string _TotalWeight;

        private string _TotalGrassWeight;

        private string _ProductNum;

       

        public MonitorMaterial_MDL() { }
        public MonitorMaterial_MDL(string vDispatchOrderNo, string vWorkOrderNo, string vMouldNo, string vMachineNo,
                string vProductNo, string vProductDesc, string vMaterialCode, string vTotalWeight, string vTotalGrassWeight, string vProductNum)
        {
            this._DispatchOrderNo = vDispatchOrderNo;
            this._WorkOrderNo = vWorkOrderNo;
            this._MouldNo = vMouldNo;
            this._MachineNo = vMachineNo;

            this._ProductNo = vProductNo;
            this._ProductDesc = vProductDesc;
            this._MaterialCode = vMaterialCode;
            this._TotalWeight = vTotalWeight;
            this._TotalGrassWeight = vTotalGrassWeight;
            this._ProductNum = vProductNum;
        }


        public string DispatchOrderNo
        {
            get { return _DispatchOrderNo; }
            set { _DispatchOrderNo = value; }
        }
        public string WorkOrderNo
        {
            get { return _WorkOrderNo; }
            set { _WorkOrderNo = value; }
        }
        public string MouldNo
        {
            get { return _MouldNo; }
            set { _MouldNo = value; }
        }
        public string MachineNo
        {
            get { return _MachineNo; }
            set { _MachineNo = value; }
        }
        public string ProductNo
        {
            get { return _ProductNo; }
            set { _ProductNo = value; }
        }
        public string ProductDesc
        {
            get { return _ProductDesc; }
            set { _ProductDesc = value; }
        }
        public string MaterialCode
        {
            get { return _MaterialCode; }
            set { _MaterialCode = value; }
        }
        public string TotalWeight
        {
            get { return _TotalWeight; }
            set { _TotalWeight = value; }
        }
        public string TotalGrassWeight
        {
            get { return _TotalGrassWeight; }
            set { _TotalGrassWeight = value; }
        }

        public string ProductNum
        {
            get { return _ProductNum; }
            set { _ProductNum = value; }
        }
    }
}
