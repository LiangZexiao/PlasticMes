using System;
using System.Collections;
using System.Collections.Generic;
using System.Text;

namespace Admin.Model.Adminis_MDL
{
    public class Department_MDL
    {
       private int _ID;
       private ArrayList _IDList;
       private string _DeptID;
       private string _DeptName;
       private string _Manger;
       private string _Remark;
       
       public Department_MDL(int _ID,string _DeptID,string _DeptName,string _Manger,string _Remark)
        {
            this._ID = _ID;
            this._DeptID = _DeptID;
            this._DeptName = _DeptName;
            this._Manger = _Manger;
            this._Remark = _Remark;
        }

       public int ID
       {
           get { return _ID; }
           set { _ID = value; }
       }
       public ArrayList IDList
       {
           get { return _IDList; }
           set { _IDList = value; }
       }
        public string DeptID
       {
           get { return _DeptID; }
           set { _DeptID = value; }
       }
        public string DeptName
       {
           get { return _DeptName; }
           set { _DeptName = value; }
       }
        public string Manger
       {
           get { return _Manger; }
           set { _Manger = value; }
       }
        public string Remark
       {
           get { return _Remark; }
           set { _Remark = value; }
       }
    }
}
