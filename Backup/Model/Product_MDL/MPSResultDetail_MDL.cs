using System;
using System.Collections.Generic;
using System.Text;

namespace Admin.Model.Product_MDL
{
    public class MPSResultDetail_MDL
    {
        private int _ID;
        private string _MasterID;
        private string _WorkOrderNo;
        private string _DeliveryDate;
        private string _DeliveryQty;

        public MPSResultDetail_MDL(){}
        public MPSResultDetail_MDL(int vID, string vMasterID, string vWorkOrderNo, string vDeliveryDate, string vDeliveryQty)
        {
            this._ID = vID;
            this._MasterID = vMasterID;
            this._WorkOrderNo = vWorkOrderNo;
            this._DeliveryDate = vDeliveryDate;
            this._DeliveryQty = vDeliveryQty;
        }

        public int ID
        {
            get { return _ID; }
            set { _ID = value; }
        }

        public string MasterID
        {
            get { return _MasterID; }
            set { _MasterID = value; }
        }

        public string WorkOrderNo
        {
            get { return _WorkOrderNo; }
            set { _WorkOrderNo = value; }
        }

        public string DeliveryDate
        {
            get { return _DeliveryDate; }
            set { _DeliveryDate = value; }
        }

        public string DeliveryQty
        {
            get { return _DeliveryQty; }
            set { _DeliveryQty = value; }
        }
    }
}
